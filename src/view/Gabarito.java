    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String respostas [] = prova.getRespostas();
        
        if(respostas[0] == "A") prova.acertos++;
        if(respostas[1] == "E") prova.acertos++;
        if(respostas[2] == "A") prova.acertos++;
        if(respostas[3] == "C") prova.acertos++;
                
        JOptionPane.showMessageDialog(null, "RESPOSTAS ENVIADAS", 
                    "SUCESSO", JOptionPane.INFORMATION_MESSAGE); 
        btnQ1.setEnabled(false);
        btnQ2.setEnabled(false);
        btnQ3.setEnabled(false);
        btnQ4.setEnabled(false);
        btnVoltar.setEnabled(true);
        btnRanking.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

