    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        String respostas [] = prova.getRespostas();
        
        if(respostas[0] != "X"){
            lblRes1.setText("Letra "+respostas[0]);
            //lblSta1.setForeground(Color.GREEN);
            lblSta1.setText(" Respondido");
            btnQ1.setText("Mudar resposta");
        }else{
            lblRes1.setText("   -");
            lblSta1.setForeground(Color.RED);
            lblSta1.setText("Sem resposta");
            btnQ1.setText("Responder");
        }
        
        if(respostas[1] != "X"){
            lblRes2.setText("Letra "+respostas[1]);
            //lblSta2.setForeground(Color.GREEN);
            lblSta2.setText(" Respondido");
            btnQ2.setText("Mudar resposta");
        }else{
            lblRes2.setText("   -");
            lblSta2.setForeground(Color.RED);
            lblSta2.setText("Sem resposta");
            btnQ2.setText("Responder");
        }
        
        if(respostas[2] != "X"){
            lblRes3.setText("Letra "+respostas[2]);
            //lblSta3.setForeground(Color.GREEN);
            lblSta3.setText(" Respondido");
            btnQ3.setText("Mudar resposta");
        }else{
            lblRes3.setText("   -");
            lblSta3.setForeground(Color.RED);
            lblSta3.setText("Sem resposta");
            btnQ3.setText("Responder");
        }
        
        if(respostas[3] != "X"){
            lblRes4.setText("Letra "+respostas[3]);
            //lblSta4.setForeground(Color.GREEN);
            lblSta4.setText(" Respondido");
            btnQ4.setText("Mudar resposta");
        }else{
            lblRes4.setText("   -");
            lblSta4.setForeground(Color.RED);
            lblSta4.setText("Sem resposta");
            btnQ4.setText("Responder");
        }
        
        lblTempo1.setText(Integer.toString(prova.tempo[0].min)+"m"+
                        Integer.toString(prova.tempo[0].seg)+"s");
        lblTempo2.setText(Integer.toString(prova.tempo[1].min)+"m"+
                        Integer.toString(prova.tempo[1].seg)+"s");
        lblTempo3.setText(Integer.toString(prova.tempo[2].min)+"m"+
                        Integer.toString(prova.tempo[2].seg)+"s");
        lblTempo4.setText(Integer.toString(prova.tempo[3].min)+"m"+
                        Integer.toString(prova.tempo[3].seg)+"s");
        
     
    }//GEN-LAST:event_formWindowActivated

